export function intensityLabel(intensity: number): string {
  if (intensity <= 1) return "Intro";
  if (intensity <= 2) return "Easy";
  if (intensity <= 3) return "Medium";
  if (intensity <= 4) return "Hard";
  return "Expert";
}

export function intensityColor(intensity: number): string {
  if (intensity <= 1) return "text-sky-400";
  if (intensity <= 2) return "text-green-400";
  if (intensity <= 3) return "text-yellow-400";
  if (intensity <= 4) return "text-orange-400";
  return "text-red-400";
}
